template<unsigned int M>
struct modint {
  int64 x;

  modint(const modint<M> &m) : x(m.x) {}
  explicit modint(const int64 _x) : x((_x >= 0 ? _x % M : M + (_x % (int) M))) {}
  explicit modint(const int _x) : x((_x >= 0 ? _x % M : M + (_x % (int) M))) {}
  explicit modint() : x(0) {}

  [[nodiscard]] modint<M> inv() const {
    return power(M - 2);
  }

  [[nodiscard]] modint<M> power(int64 exp) const {
    modint<M> res(1), base(x);
    while (exp) {
      if (exp & 1) res *= base;
      base *= base;
      exp >>= 1;
    }
    return res;
  }

  [[nodiscard]] modint<M> power(modint<M> &exp) const {
    return power(exp.x);
  }

  modint<M> &operator=(const int _x) {
    x = (_x >= 0 ? _x % M : M + (_x % M));
    return *this;
  }

  modint<M> &operator=(const int64 _x) {
    x = (_x >= 0 ? _x % M : M + (_x % M));
    return *this;
  }

  modint<M> &operator=(const modint<M> &other) {
    x = other.x;
    return *this;
  }

  modint<M> &operator+=(const modint<M> &other) {
    x += other.x;
    x -= (x >= M ? M : 0);
    return *this;
  }

  modint<M> &operator-=(const modint<M> &other) {
    x += M - other.x;
    x -= (x >= M ? M : 0);
    return *this;
  }

  modint<M> &operator*=(const modint<M> &other) {
    x *= other.x;
    x %= M;
    return *this;
  }

  modint<M> &operator/=(const modint<M> &other) {
    x = (*this * other.inv()).x;
    return *this;
  }

  friend modint<M> operator+(const modint<M> &lhs, const modint<M> &rhs) {
    return (modint(lhs) += rhs);
  }

  friend modint<M> operator-(const modint<M> &lhs, const modint<M> &rhs) {
    return (modint(lhs) -= rhs);
  }

  friend modint<M> operator*(const modint<M> &lhs, const modint<M> &rhs) {
    return (modint(lhs) *= rhs);
  }

  friend modint<M> operator/(const modint<M> &lhs, const modint<M> &rhs) {
    return (modint(lhs) /= modint(rhs));
  }

  friend bool operator==(const modint<M> &lhs, const modint<M> &rhs) {
    return (lhs.x == rhs.x);
  }

  friend bool operator!=(const modint<M> &lhs, const modint<M> &rhs) {
    return (lhs.x != rhs.x);
  }

  friend std::ostream &operator<<(std::ostream &os, const modint<M> &m) {
    return (os << m.x);
  }

  friend std::istream &operator>>(std::istream &in, modint<M> &m) {
    return (in >> m.x);
  }

  modint<M> &operator++() {
    x++;
    x = (x == M ? 0 : x);
    return *this;
  }

  modint<M> &operator--() {
    x = (x == 0 ? M : x);
    x--;
    return *this;
  }

  modint<M> operator++(int) {
    modint result = *this;
    ++*this;
    return result;
  }

  modint<M> operator--(int) {
    modint result = *this;
    --*this;
    return result;
  }
};

using mint = modint<kMod>;

enum Spin {
  none = 0,
  negative = -1,
  positive = 1
};

struct Cell {
  int x;
  int y;
  Spin s;
};

Spin opposite(const Spin &s) {
  if (s == Spin::none) assert(false);
  if (s == Spin::negative) return Spin::positive;
  return Spin::negative;
}

inline void solution() {
  const int n = Read::Int();
  const int m = Read::Int();
  const int k = Read::Int();

  vector<Cell> electron(k);
  for (auto &[x, y, s] : electron) {
    s = (Read::Char() == '-' ? Spin::negative : Spin::positive);
    x = Read::Int();
    y = Read::Int();
  }

  mint ans_x(1);
  mint ans_y(1);
  mint common(2);

  map<int, Spin> fix_x;
  for (const auto [x, y, s] : electron) {
    const Spin expected = ((y & 1) ? s : opposite(s));
    if (fix_x[x] == Spin::none) {
      fix_x[x] = expected;
    }
    if (fix_x[x] == expected) continue;
    ans_x = 0;
    break;
  }

  map<int, Spin> fix_y;
  for (const auto [x, y, s] : electron) {
    const Spin expected = ((x & 1) ? s : opposite(s));
    if (fix_y[y] == Spin::none) {
      fix_y[y] = expected;
    }
    if (fix_y[y] == expected) continue;
    ans_y = 0;
    break;
  }

  ans_x *= mint(2).power(n - fix_x.size());
  ans_y *= mint(2).power(m - fix_y.size());

  for (const auto [x, y, s] : electron) {
    const Spin expected = (((x + y) & 1) ? Spin::positive : Spin::negative);
    if (expected == s) continue;
    common--;
    break;
  }

  for (const auto [x, y, s] : electron) {
    const Spin expected = (((x + y) & 1) ? Spin::negative : Spin::positive);
    if (expected == s) continue;
    common--;
    break;
  }

  cout << ans_x + ans_y - common << '\n';
}

